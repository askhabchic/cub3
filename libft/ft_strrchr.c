#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	int	i;

	i = ft_strlen(str);
	while (i >= 0)
	{
		if (ch == str[i])
			return ((char *)str + i);
		--i;
	}
	return (NULL);
}
